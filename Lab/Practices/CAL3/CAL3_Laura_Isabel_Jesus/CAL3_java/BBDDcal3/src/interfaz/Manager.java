    private void bsiguienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bsiguienteMouseClicked
        // product
        float i = (float)25;
        if (rProduct.isSelected()){
            if (rBinsert.isSelected()){
                try {
                    JOptionPane.showMessageDialog(this,
                        Product.insert(usuario, contra,
                            Integer.parseInt(t1.getText()),
                            t2.getText(), Integer.parseInt(t3.getText()), i, Integer.parseInt(t4.getText()),
                            Integer.parseInt(t5.getText()), t6.getText(),
                            t7.getText()));
                } catch (SQLException ex) {
                    Logger.getLogger(Cashier.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (rBdelete.isSelected()){
                JOptionPane.showMessageDialog(this, Product.delete(usuario, contra));
            } else if (rBdeleteOne.isSelected()){
                JOptionPane.showMessageDialog(this, Product.delete_one(usuario,
                    contra, Integer.parseInt(t1.getText())));
        } else if (rBupdate.isSelected()){
            JOptionPane.showMessageDialog(this, Product.update(usuario, contra,
                Integer.parseInt(t1.getText()), t2.getText()));
        } else if (rBselect.isSelected()){
            JOptionPane.showMessageDialog(this, Product.select(usuario, contra));
        }
        }
        // purchased
        else if (rPurchased.isSelected()){
            if (rBinsert.isSelected()){
                JOptionPane.showMessageDialog(this, Evaluates.insert(usuario,
                    contra, t1.getText(), t2.getText(),
                    Integer.parseInt(t3.getText())));
        } else if (rBdelete.isSelected()){
            JOptionPane.showMessageDialog(this, Evaluates.delete(usuario, contra));
        } else if (rBdeleteOne.isSelected()){
            JOptionPane.showMessageDialog(this, Evaluates.delete_one(usuario,
                contra, t1.getText()));
        } else if (rBupdate.isSelected()){
            JOptionPane.showMessageDialog(this, Evaluates.update(usuario,
                contra, t1.getText(), Integer.parseInt(t3.getText())));
        } else if (rBselect.isSelected()){
            JOptionPane.showMessageDialog(this, Evaluates.select(usuario, contra));
        }
        }
        // ticket
        else if (rTicket.isSelected()){
            if (rBinsert.isSelected()){
                JOptionPane.showMessageDialog(this, Ticket.insert(usuario, contra,
                    t1.getText(), t2.getText(), t3.getText(), false,
                    Integer.parseInt(t4.getText()), Integer.parseInt(t5.getText()),
                    t6.getText(), Integer.parseInt(t7.getText()), false, Integer.parseInt(t8.getText())));
        } else if (rBdelete.isSelected()){
            JOptionPane.showMessageDialog(this, Ticket.delete(usuario, contra));
        } else if (rBdeleteOne.isSelected()){
            JOptionPane.showMessageDialog(this, Ticket.delete_one(usuario,
                contra, t1.getText()));
        } else if (rBupdate.isSelected()){
            JOptionPane.showMessageDialog(this, Ticket.update(usuario,
                contra, Integer.parseInt(t1.getText()), Integer.parseInt(t2.getText())));
        } else if (rBselect.isSelected()){
            JOptionPane.showMessageDialog(this, Ticket.select(usuario, contra));
        }
        }
        // returned
        else if (rReturned.isSelected()){
            if (rBinsert.isSelected()){
                JOptionPane.showMessageDialog(this, Returned.insert(usuario,
                    contra, Integer.parseInt(t1.getText()),
                    Integer.parseInt(t2.getText()),
                    (float) Integer.parseInt(t3.getText())));
        } else if (rBdelete.isSelected()){
            JOptionPane.showMessageDialog(this, Returned.delete(usuario, contra));
        } else if (rBdeleteOne.isSelected()){
            JOptionPane.showMessageDialog(this, Returned.delete_one(usuario,
                contra, Integer.parseInt(t1.getText())));
        } else if (rBupdate.isSelected()){
            JOptionPane.showMessageDialog(this, Returned.update(usuario,
                contra, Integer.parseInt(t1.getText()),
                Integer.parseInt(t1.getText())));
        } else if (rBselect.isSelected()){
            JOptionPane.showMessageDialog(this, Returned.select(usuario, contra));
        }
        }
    }//GEN-LAST:event_bsiguienteMouseClicked

